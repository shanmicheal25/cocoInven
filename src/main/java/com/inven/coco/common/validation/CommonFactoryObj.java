package com.inven.coco.common.validation;

public class CommonFactoryObj {

	private String header;
	private String content;
	private int dialogWidth;
	private int dialogHeight;
	private boolean checkOpenCommonDialog = false;
	public String getHeader() {
		return header;
	}
	public void setHeader(String header) {
		this.header = header;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getDialogWidth() {
		return dialogWidth;
	}
	public void setDialogWidth(int dialogWidth) {
		this.dialogWidth = dialogWidth;
	}
	public int getDialogHeight() {
		return dialogHeight;
	}
	public void setDialogHeight(int dialogHeight) {
		this.dialogHeight = dialogHeight;
	}
	public boolean isCheckOpenCommonDialog() {
		return checkOpenCommonDialog;
	}
	public void setCheckOpenCommonDialog(boolean checkOpenCommonDialog) {
		this.checkOpenCommonDialog = checkOpenCommonDialog;
	}
	
	
}
